#  string methods

name = 'Nolan'
# returns how long the length of a name is
print(len(name))
# returns index of character
print(name.find('N'))
#  capitalize
print(name.capitalize())
# uppercase
print(name.upper())
# lowercase
print(name.lower())
# return true of false depending on the strings
print(name.isdigit())
# checks if your string contains only alphabetical characters
print(name.isalpha())
# counts how many characters
print(name.count('n'))
# replace characters
print(name.replace('n', 'c'))
# multiply string
print(name * 3)
