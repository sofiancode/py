
# booleans and numbers
print(1 == 10)  # is equal
print(1 != 10)  # is not equal
print(10 > 10)

# booleans and lists and strings
print(1 in [1, 2, 3, 4])
print('e' in 'hello')
print(4 not in [1, 2, 3, 4])

# booleans by themeselves
print(not True)

eDict = {1: 'one', 2: 'two', 3: 'three'}

print(1 in eDict)
print('four' in eDict.values())

